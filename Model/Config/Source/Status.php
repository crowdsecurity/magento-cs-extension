namespace CrowdSec\Engine\Model\Config\Source;

use CrowdSec\Engine\Api\Data\EventInterface;
use Magento\Framework\Data\OptionSourceInterface;

class Status implements OptionSourceInterface
{
    public function toOptionArray()
    {
        $result = [];
        foreach ($this->getOptions() as $value => $label) {
            $result[] = [
                'value' => $value,
                'label' => $label,
            ];
        }

        return $result;
    }

    public function getOptions()
    {
        return [
            EventInterface::STATUS_CREATED => __('Created'),
            EventInterface::STATUS_ALERT_TRIGGERED => __('Alert triggered'),
            EventInterface::STATUS_SIGNAL_PUSHED => __('Signal pushed'),
        ];
    }
}

